// ── AUTO-LOADER ──────────────────────────────────────────────────────
// Finds every <img data-img="key"> and sets its src from IMAGES above.
// Runs automatically — you don't need to call this anywhere.
(function () {
  function applyImages() {
    document.querySelectorAll('img[data-img]').forEach(function (el) {
      var key = el.getAttribute('data-img');
      if (IMAGES[key]) el.src = IMAGES[key];
    });
  }
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyImages);
  } else {
    applyImages();
  }
})();
